# /////Strings Slicing & Operations on String///////


names = "prasad,karan"
print(names[0:6])    # used to find length of a string (output is prasad)

fruit = "Mango"
mangoLen = len(fruit)
print(mangoLen)
print(fruit[0:3])

flower = "Rose"
roseLen = len(flower)
print(roseLen)
print(flower[0:2]) 

car = "Scorpio"
scorpioLen = len(car)
print(scorpioLen)
print(car[0:-4])   # it means here place of -4 it automatically get len(car)-4 = 3 

